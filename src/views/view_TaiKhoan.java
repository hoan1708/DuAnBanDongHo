    private void hideshowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hideshowMouseClicked
        if (hish == false) {
            hish = true;
            hideshow.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/hide.png")));
            txt_pass2.setEchoChar((char) 0);
        } else {
            hish = false;
            hideshow.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/show.png")));
            txt_pass2.setEchoChar('\u25cf');
        }

    }//GEN-LAST:event_hideshowMouseClicked

    private void btnDoiMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiMKActionPerformed
        // TODO add your handling code here:
        if (us1.getMatKhau().equalsIgnoreCase(txt_pass1.getText()) == false) {
            JOptionPane.showMessageDialog(jTabbedPane3, "Nhập Sai Mật Khẩu Cũ ! Vui lòng nhập lại ");
        } else if (txt_pass2.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(jTabbedPane3, "Mật Khẩu Mới Không Được Để Trống!");
        } else if (txt_pass3.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(jTabbedPane3, "Xác Nhận Mật Khẩu Mới Không Được Để Trống!");
        } else if (txt_pass3.getText().equalsIgnoreCase(txt_pass2.getText()) == false) {
            JOptionPane.showMessageDialog(jTabbedPane3, "Xác Nhận Mật Khẩu Mới chưa đúng ! Vui lòng nhập lại");
        } else {
            User usUpdate = new User(us1.getTaiKhoan(), txt_pass2.getText(), us1.getChucVu(), us1.getTrangThai(), us1.getTenUSer(), us1.getGioiTinh(), us1.getDate());
            new UserService().Update(usUpdate, us1.getTaiKhoan());
            JOptionPane.showMessageDialog(jTabbedPane3, "Đổi Mật Khẩu Thành Công! Vui lòng đăng nhập lại");
            System.exit(0);
        }
    }//GEN-LAST:event_btnDoiMKActionPerformed


