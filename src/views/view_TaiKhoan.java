    private void fillData(User us1) throws ParseException {
        txt_Ten.setText(us1.getTenUSer());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date date1 = sdf.parse(us1.getDate());
        txt_ngaySinh.setDate(date1);
        if (us1.getGioiTinh() == 0) {
            rdNam.setSelected(true);
        } else {
            rdNu.setSelected(true);
        }
        if (us1.getTrangThai() == 0) {
            cbbTrangThai.setSelectedIndex(0);
        } else {
            cbbTrangThai.setSelectedIndex(1);
        }
        if (us1.getChucVu() == 0) {
            cbbChucVu.setSelectedIndex(0);
        } else {
            cbbChucVu.setSelectedIndex(1);
        }
    }

