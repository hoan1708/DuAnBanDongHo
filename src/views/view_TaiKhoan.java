    private void btnSuaTTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaTTActionPerformed
        // TODO add your handling code here:
        int chucVu = cbbChucVu.getSelectedIndex();
        int trangThai = cbbTrangThai.getSelectedIndex();
        int gioiTinh = 0;
        if (rdNam.isSelected()) {
            gioiTinh = 0;
        } else {
            gioiTinh = 1;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date1 = sdf.format(txt_ngaySinh.getDate());
        User usUpdate = new User(us1.getTaiKhoan(), us1.getMatKhau(), chucVu, trangThai, txt_Ten.getText(), gioiTinh, date1);
        if (new UserService().Update(usUpdate, us1.getTaiKhoan())) {
            JOptionPane.showMessageDialog(jTabbedPane3, "Sửa Thành Công");
        } else {
            JOptionPane.showMessageDialog(jTabbedPane3, "Sửa thất bại");
        }
        JOptionPane.showMessageDialog(jTabbedPane3, "Vui lòng đăng nhập lại");
        System.exit(0);
    }//GEN-LAST:event_btnSuaTTActionPerformed

